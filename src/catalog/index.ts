/** Look up any catalogued part by id across every category. */
export function findPart(id: string) {
  for (const [category, list] of Object.entries(catalog)) {
    const hit = (list as Array<{ id: string }>).find((p) => p.id === id)
    if (hit) return { category, part: hit }
  }
  return null
}
