/**
 * Inverters whose optimizers hold the string at a fixed voltage.
 *
 * These break the usual NEC 690.7 string-sizing model: because each optimizer
 * regulates its own output, string length is bounded by *optimizer count*, not
 * by cold-temperature Voc. Sizing one of these with the temperature-correction
 * method produces a wrong answer, so the engine branches on this set.
 */
export const OPTIMIZER_BASED_INVERTERS = new Set(['solaredge'])

export interface OptimizerStringLimits {
  min_per_string: number
  max_per_string: number
  /**
   * Beyond this count the accumulated per-optimizer standby voltage
   * (~1 V each) breaches the NEC 690.12 30 V rapid-shutdown limit.
   */
  rapid_shutdown_max: number
}

